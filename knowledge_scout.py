def update_kb(items, today):
    if not items: return
    if not KB_PATH.exists():
        KB_PATH.parent.mkdir(parents=True, exist_ok=True)
        KB_PATH.touch()
    with open(KB_PATH, "r", encoding="utf-8") as f: content = f.read()
    if not content.strip():
        content = "# 灵枢知识库\n\n"
        for ccode in CATEGORIES_ORDER:
            content += f"\n## {ccode}\n<!-- 新条目自动追加在此行下方 -->\n\n"
        content += "\n## 去重索引 (Dedup Index)\n<!-- 格式：MD5(标题+URL) | 采集日期 -->\n"
    seq = max([int(s) for s in re.findall(r"KB-(\d{4})", content)], default=0) + 1
    for item in items:
        cat = item["category"]; marker = f"## {cat}"; idx = content.find(marker)
        if idx == -1: continue
        mi = content.find("<!-- 新条目自动追加在此行下方 -->", idx)
        if mi == -1: continue
        entry = f"| KB-{seq:04d} | {item['title']} | {item['source']} | {item['description'][:60]} | [→]({item['url']}) | {today} |"
        content = content[:mi+len("<!-- 新条目自动追加在此行下方 -->")] + "\n" + entry + content[mi+len("<!-- 新条目自动追加在此行下方 -->"):]
        seq += 1
    di = content.find("## 去重索引")
    if di != -1:
        dmi = content.find("<!-- 格式：MD5(标题+URL) | 采集日期 -->", di)
        if dmi != -1:
            pos = dmi + len("<!-- 格式：MD5(标题+URL) | 采集日期 -->")
            content = content[:pos] + "\n" + "\n".join([f"| {i['dedup_key']} | {today} |" for i in items]) + content[pos:]
    with open(KB_PATH, "w", encoding="utf-8") as f: f.write(content)
    print(f"✅ KB updated: +{len(items)} items")

