def scout_github(config, keys):
    items = []
    token = config.get("api_keys", {}).get("github_token", "")
    use_api = bool(token and (token.startswith("ghp_") or token.startswith("github_pat_")))
    if use_api:
        headers = {"Accept": "application/vnd.github.v3+json", "Authorization": f"token {token}"}
        for label, q in [("大模型", "LLM large language model"), ("AI Agent", "AI agent framework"), ("ComfyUI", "ComfyUI workflow"), ("AI视频", "AI video generation"), ("AI影视", "AI filmmaking")]:
            try:
                resp = requests.get(f"https://api.github.com/search/repositories?q={quote_plus(q)}&sort=stars&order=desc&per_page=3", headers=headers, timeout=15)
                if resp.status_code != 200: time.sleep(2); continue
                for repo in resp.json().get("items", []):
                    fn, hu = repo.get("full_name", ""), repo.get("html_url", "")
                    if not fn: continue
                    d = (repo.get("description") or "")[:100]
                    s = repo.get("stargazers_count", 0)
                    dk = make_dedup_key(fn, hu)
                    if dk in keys: continue
                    items.append({"title": fn, "url": hu, "description": human_summary(fn, f"[{label}] {d}", "GitHub", f"⭐{s}"),
                                  "source": "GitHub", "category": classify_item(fn, d), "dedup_key": dk,
                                  "image": repo.get("owner", {}).get("avatar_url", ""), "extra": f"⭐{s}", "paid": detect_paid(fn, d)})
                time.sleep(1)
            except: pass
    else:
        ai_kw = ["ai", "agent", "llm", "diffusion", "comfyui", "video", "film", "mcp", "workflow", "gpt", "claude", "gemini", "deepseek", "aigc"]
        seen = set()
        for turl in ["https://github.com/trending?since=daily", "https://github.com/trending/python?since=daily"]:
            try:
                resp = requests.get(turl, headers={"User-Agent": "Mozilla/5.0"}, timeout=15)
                if resp.status_code != 200: continue
                for block in re.findall(r'<article class="Box-row">(.*?)</article>', resp.text, re.DOTALL)[:20]:
                    hm = re.search(r'href="(/[^"]+?)"', block)
                    if not hm: continue
                    fn = hm.group(1).strip("/")
                    if fn in seen or "/" not in fn: continue
                    seen.add(fn)
                    dm = re.search(r'<p class="col-9[^"]*">\s*(.*?)\s*</p>', block, re.DOTALL)
                    d = re.sub(r"<[^>]+>", "", dm.group(1)).strip()[:80] if dm else ""
                    if not any(kw in (fn + " " + d).lower() for kw in ai_kw): continue
                    hu = f"https://github.com/{fn}"
                    dk = make_dedup_key(fn, hu)
                    if dk in keys: continue
                    items.append({"title": fn, "url": hu, "description": human_summary(fn, d, "GitHub", "🔥Trending"),
                                  "source": "GitHub", "category": classify_item(fn, d), "dedup_key": dk,
                                  "image": "", "extra": "🔥Trending", "paid": detect_paid(fn, d)})
                time.sleep(0.5)
            except: pass
    print(f"  GitHub: {len(items)}"); return items

