def classify_item(title, desc=""):
    text = f"{title} {desc}".lower()
    best_cat, best_score = "CAT-TREND", 0
    for cat_code in CATEGORIES_ORDER:
        if cat_code not in CATEGORY_KEYWORDS: continue
        score = sum(2 if kw in text else 0 for kw in CATEGORY_KEYWORDS[cat_code])
        if score > best_score: best_score, best_cat = score, cat_code
    return best_cat

def make_dedup_key(title, url):
    return hashlib.md5(f"{title}|{url}".encode("utf-8")).hexdigest()

def load_existing_dedup_keys():
    keys = set()
    if not KB_PATH.exists(): return keys
    with open(KB_PATH, "r", encoding="utf-8") as f:
        for line in f:
            m = re.match(r"^\| ([a-f0-9]{32}) \|", line)
            if m: keys.add(m.group(1))
    return keys

def human_summary(title, desc, source, extra=""):
    smap = {"GitHub": "GitHub", "Hugging Face": "HF", "魔搭社区": "魔搭", "Bilibili": "B站",
            "YouTube": "YouTube", "抖音": "抖音", "小红书": "小红书", "微博": "微博",
            "微信公众号": "微信", "TikTok": "TikTok", "今日头条": "头条", "知乎": "知乎",
            "DuckDuckGo": "搜索", "Google News": "新闻"}
    parts = [smap.get(source, source)]
    if extra: parts.append(extra)
    if desc:
        clean = re.sub(r"[⭐📥▶💰🆓]", "", desc).strip()
        clean = re.sub(r"\s+", " ", clean)[:80]
        if clean: parts.append(clean)
    return " | ".join(parts) if parts else title

def detect_paid(title, desc):
    return any(kw in f"{title} {desc}".lower() for kw in ["paid", "premium", "pro", "subscription", "付费", "会员", "订阅", "$"])

def _extract_ddg_url(raw):
    if "duckduckgo.com/l/?uddg=" in raw:
        m = re.search(r"uddg=([^&]+)", raw)
        if m:
            try: return unquote(m.group(1))
            except: return "https://" + raw.lstrip("/")
    if raw.startswith("//"): return "https://" + raw.lstrip("/")
    return raw if raw.startswith("http") else raw

